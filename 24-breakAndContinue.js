// Break Statement 
console.log("Break Statement Example ");
for(let i = 0;i<10;i++)
{
    if(i==5)
    {
        break;
    }
    console.log(i);
}

// Continue Statement 
console.log("Continue Statement Example ");
for(let i = 1;i<=10;i++)
{
    if(i%2 == 0)
    {
        continue;
    }
    console.log(i);
}
