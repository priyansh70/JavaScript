// Boolean and Comparision
// Boolean - true and false 

let num1 = 10;
let num2 = 20;

console.log(num1 == num2);
console.log(num1 >= num2);
console.log(num1 <= num2);

// Difference between == and ===
num1 = 15;
num2 = '15';
console.log(num1 == num2); //check only value
console.log(num1 != num2); //check not equal
console.log(num1 !== num2); //check not equal
console.log(num1 === num2); //check value with data type